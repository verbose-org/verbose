"""Verbose-portable mod-L arithmetic (Ed25519 group order L = 2^252 + d0),
prototyped in Python with NO big-int in the core — only per-limb int ops that
translate directly to a Verbose let-chain. Validated against big-int %L.

ref10 sc_reduce / sc_muladd shape: 21-bit limbs, base 2^21. The reduction uses
L = 2^252 + d0 (d0 ~ 2^125), so 2^252 = -d0 mod L; high limbs (carrying weight
>= 2^252) are folded down by multiplying by the d0 limbs and subtracting.

This mirrors the well-known ref10 group-order reduction exactly (the constants
666643, 470296, 654183, -997805, 136657, -683901 are the 21-bit limbs of d0).
"""
